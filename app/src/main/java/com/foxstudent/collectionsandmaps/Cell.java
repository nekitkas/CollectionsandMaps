package com.foxstudent.collectionsandmaps;

public class Cell {
    private String name;
    private String result;


    public Cell() {
    }

    public Cell(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getResult() {
        return result;
    }

}
